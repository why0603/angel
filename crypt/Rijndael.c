void CopyString(unsigned char dest[],char* src,unsigned int srcLen)
{
	if ((NULL != dest) && (NULL != src))
	{
		if (srcLen <= 0)
		{
			srcLen = strlen(src);
		}
		while (srcLen-- >0)
		{
			dest[srcLen] = src[srcLen];
		}
	}
}

void StateToChars(unsigned char* output,unsigned long state[],unsigned int stateLen)
{
	unsigned int round;
	for (round=0; round<stateLen; round++)
	{
		*(output + round) = (unsigned char)((state[round] >> 24) & 0xff);
		*(output + stateLen + round) = (unsigned char)((state[round] >> 16) & 0xff);
		*(output + stateLen * 2 + round) = (unsigned char)((state[round] >> 8) & 0xff);
		*(output + stateLen * 3 + round) = (unsigned char)(state[round] & 0xff);
	}
}


//伽罗瓦域计算
void MixColumnStr(RijndaelContextPtr context,unsigned char* r)
{
	//r的拷贝
	unsigned char a[4];
	//r * 0x2的伽罗瓦域计算
	unsigned char b[4];
	unsigned char c;
	//unsigned char h;
	
	//a[n] ^ b[n] is element n multiplied by 3 in Rijndael's Galois field */ 
	for(c=0; c<4; c++) 
	{
		a[c] = r[c];
		//这行代码代替下面所有的代码
		//依据的规则是任何数与0异或其值不变
		b[c] = XTIME(r[c]);
		/*
		h = r[c] & 0x80;
		b[c] = r[c] << 1;
		if(h == 0x80) 
		{
			b[c] ^= 0x1b;
		}*/
	}
	//0x01、0x02、0x03、0x09、0x0b、0x0d 和 0x0e 来相乘
	//由于GF(256)域的值永远在0-255之间，所以获取2,3,9,b,d,e的
	//GF域值时可以通过查表法的
	//其实是一个矩阵c(x) = x^4 + 1 = 3*x^3 + 2*x^2 + x + 1
	//他的逆运算为c^-1(x)=11*x^3 + 13*x^2 + 9*x + 14
	//换成矩阵则是
	/*
	 * [2 3 1 1] [a0]   
	 * [1 2 3 1] [a1]
	 * [1 1 2 3] [a2]
	 * [3 1 1 2] [a3]
	 * b0 = 2 * a0 + a3 + a2 + 3 * a1
	 * b1 = 2 * a1 + a0 + a3 + 3 * a2
	 * b2 = 2 * a2 + a1 + a0 + 3 * a3
	 * b3 = 2 * a3 + a2 + a1 + 3 * a0
	 * 这里最高只有乘以3，即（x+1)=(2+1)因此b[]只要保存r[]*2的值即可
	 * b[1] ^ a[1] = 3 * a[1]
	 */
	/* 2 * a0 + a3 + a2 + 3 * a1 */
	r[0] = b[0] ^ a[3] ^ a[2] ^ b[1] ^ a[1]; 
	/* 2 * a1 + a0 + a3 + 3 * a2 */
	r[1] = b[1] ^ a[0] ^ a[3] ^ b[2] ^ a[2];
	/* 2 * a2 + a1 + a0 + 3 * a3 */
	r[2] = b[2] ^ a[1] ^ a[0] ^ b[3] ^ a[3];
	/* 2 * a3 + a2 + a1 + 3 * a0 */
	r[3] = b[3] ^ a[2] ^ a[1] ^ b[0] ^ a[0];
}

void InvMixColumnStr(RijndaelContextPtr context,unsigned char* r)
{
	//r的拷贝
	unsigned char a[4];
	//r * 0x2的伽罗瓦域计算
	unsigned char t9[4];//*0x9
	unsigned char tb[4];//*0xb
	unsigned char td[4];//*0xd
	unsigned char te[4];//*0xe
	unsigned char c;
	//unsigned char h;
	
	//a[n] ^ b[n] is element n multiplied by 3 in Rijndael's Galois field */ 
	//0x09、0x0b、0x0d 和 0x0e
	for(c=0; c<4; c++) 
	{
		a[c] = r[c];
		//这行代码代替下面所有的代码
		//依据的规则是任何数与0异或其值不变
		t9[c] = MULTIPLY(r[c],0x9);
		tb[c] = MULTIPLY(r[c],0xb);
		td[c] = MULTIPLY(r[c],0xd);
		te[c] = MULTIPLY(r[c],0xe);
	}
	//矩阵c(x) = x^4 + 1 = 3*x^3 + 2*x^2 + x + 1
	//的逆运算为c^-1(x)=11*x^3 + 13*x^2 + 9*x + 14
	//换成矩阵则是
	/*
	 * [14 11 13  9] [a0]   
	 * [ 9 14 11 13] [a1]
	 * [13  9 14 11] [a2]
	 * [11 13  9 14] [a3]
	 * b0 = 14 * a0 + 11 * a1 + 13 * a2 +  9 * a3
	 * b1 =  9 * a0 + 14 * a1 + 11 * a2 + 13 * a3
	 * b2 = 13 * a0 +  9 * a1 + 14 * a2 + 11 * a3 
	 * b3 = 11 * a0 + 13 * a1 +  9 * a2 + 14 * a3
	 * 这里最高只有乘以3，即（x+1)=(2+1)因此b[]只要保存r[]*2的值即可
	 * b[1] ^ a[1] = 3 * a[1]
	 */
	r[0] = te[0] ^ tb[1] ^ td[2] ^ t9[3];
	r[1] = t9[0] ^ te[1] ^ tb[2] ^ td[3];
	r[2] = td[0] ^ t9[1] ^ te[2] ^ tb[3];
	r[3] = tb[0] ^ td[1] ^ t9[2] ^ te[3];
}


/*
//从字节数组s中获取第n个整数,w为列数，及一行包含多少列，为字节长度/32/4
//Rijndael使用竖排的方式来获取整数，比如字节S0,S1,S2.......S(n-1)
//(256位)
则排列为
S0,S1,S2,S3,S4,S5,S6,S7
S8,S9..................
S16,
S24
其中字节S0,S8,S16,S24组成一个整型数据
*/
#define GETINT32(s,n,w) ((s)[(n)] << 24 |		\
	(s)[(w)+(n)] << 16 |		\
	(s)[2*(w)+(n)] << 8 |	\
	(s)[3*(w)+(n)])

//s-整型数据 n所在的列序号,w总列数,o输出字节序列
#define SETINT32(s,n,w,o) ((o)[(n)] = ((s) >> 24) ,		\
	(o)[(w)+(n)] = ((s) >> 16) ,		\
	(o)[2*(w)+(n)] = ((s) >> 8) ,	\
	(o)[3*(w)+(n)] = ((unsigned char)(s))

//必须保证v是无符号数,否则会出错的
#define SUBBYTE(v) ((sbox[((v)>>24)&0xff] << 24)	|	\
	(sbox[((v)>>16)&0xff] << 16)	|	\
	(sbox[((v)>>8)&0xff] << 8)	|	\
	(sbox[v&0xff]))

#define INVSUBBYTE(v) ((rbox[((v)>>24)&0xff] << 24)	|	\
	(rbox[((v)>>16)&0xff] << 16)	|	\
	(rbox[((v)>>8)&0xff] << 8)	|	\
	(rbox[v&0xff]))

//伽罗瓦域,v乘以 { 2 } //27 = 0x1b
#define XTIME(v)	(((v)<<1) ^ ((((v)>>7) & 1) * 27))   

//伽罗瓦域,x 乘以 y
#define MULTIPLY(x,y) ((((y) & 1) * x)  \
      ^ (((y)>>1 & 1) * XTIME(x)) \
      ^ (((y)>>2 & 1) * XTIME(XTIME(x))) \
      ^ (((y)>>3 & 1) * XTIME(XTIME(XTIME(x)))) \
      ^ (((y)>>4 & 1) * XTIME(XTIME(XTIME(XTIME(x))))) \
      ^ (((y)>>5 & 1) * XTIME(XTIME(XTIME(XTIME(XTIME(x)))))) \
      ^ (((y)>>6 & 1) * XTIME(XTIME(XTIME(XTIME(XTIME(XTIME(x))))))) \
      ^ (((y)>>7 & 1) * XTIME(XTIME(XTIME(XTIME(XTIME(XTIME(XTIME(x)))))))) \
     )



